    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = fieldUserName.getText();
        String password = String.valueOf(fieldPass.getPassword());
        String hakAkses;

        if (radioAdmin.isSelected()) {
            hakAkses = radioAdmin.getText();
        } else {
            hakAkses = radioOperator.getText();
        }
        //validasi input data
        if (username.isEmpty() || password.isEmpty() || hakAkses.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Isi Form Login");
        } else {
            //validasi login
            PegawaiDAO dao = new PegawaiDAOImpl();
            pegawai = dao.login(username, password, hakAkses);
            if (pegawai != null) {
                hakakses = pegawai.getHakAkses();
            }
            // cek hasil login
            if (pegawai != null && hakakses.equalsIgnoreCase("Admin")) {
                btnPegawai.setEnabled(true);
                btnLogout.setEnabled(true);
                btnLogin.setEnabled(false);
                btnPakaian.setEnabled(true);
                btnReseller.setEnabled(true);
                btnTransKonsumen.setEnabled(true);
                btnTransReseller.setEnabled(true);
                lblLogin.setText(pegawai.getNama());
                lblStatus.setText("Admin");
                JOptionPane.showMessageDialog(this,
                        "Login berhasil : " + pegawai.getNama());
            } else if (pegawai != null && hakakses.equalsIgnoreCase("Operator")) {
                btnLogout.setEnabled(true);
                btnLogin.setEnabled(false);
                lblLogin.setText(pegawai.getNama());
                lblStatus.setText("Operator");
                btnReseller.setEnabled(true);
                btnPakaian.setEnabled(true);
                btnTransKonsumen.setEnabled(true);
                btnTransReseller.setEnabled(true);
                JOptionPane.showMessageDialog(this,
                        "Login berhasil : " + pegawai.getNama());
            } else {
                fieldUserName.setText("");
                fieldPass.setText("");
                JOptionPane.showMessageDialog(this,
                        "Login gagal");
            }
            {
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

