    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int baris = tableReseller.getSelectedRow();
        if (baris >= 0) {
            //mengambil data reseller
            Reseller resellerTerpilih = arrayReseller.get(baris);

            //tampil form
            FormUbahReseller fur = new FormUbahReseller(null, true, resellerTerpilih);
            fur.setLocationRelativeTo(null);
            fur.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Reseller yang akan diubah");
        }

    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = tableReseller.getSelectedRow();
        if (baris >= 0) {
            //mengambil data reseller
            Reseller resellerTerpilih = arrayReseller.get(baris);

            int aksiUser = JOptionPane.showConfirmDialog(this, "Apakah Anda "
                    + "yakin akan menghapus data "
                    + resellerTerpilih.getNama(), "Hapus Reseller",
                    JOptionPane.YES_NO_OPTION);
            if (aksiUser == JOptionPane.YES_OPTION) {
                String id = resellerTerpilih.getIdReseller();
                ResellerDAO dao = new ResellerDAOImpl();
                boolean sukses;
                sukses = dao.hapusReseller(id);
                if (sukses) {
                    JOptionPane.showMessageDialog(this, "Reseller berhasil dihapus");        
                } else {
                    JOptionPane.showMessageDialog(this, "Reseller berhasil dihapus");
                }
            }
            } else {
                JOptionPane.showMessageDialog(this, "Pilih Reseller");
            }
            getData(null);
    }//GEN-LAST:event_btnHapusActionPerformed

