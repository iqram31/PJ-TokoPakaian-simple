    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        String nama = fieldNama.getText();
        String harga = fieldHarga.getText();
        String stok = fieldStok.getText();
        String kode = lblKode.getText();
        String kat;

        if (radioPria.isSelected()) {
            kat = radioPria.getText();
        } else {
            kat = radioWanita.getText();
        }

        if ((nama != null && !nama.isEmpty())
                && (kat != null && !kat.isEmpty())
                && (harga != null && !harga.isEmpty())
                && (stok != null && !stok.isEmpty())) {
            Pakaian pakaian = new Pakaian();
            int stok2 = Integer.parseInt(stok);
            int harga2 = Integer.parseInt(harga);
            pakaian.setNama(nama);
            pakaian.setKatPakaian(kat);
            pakaian.setHarga(harga2);
            pakaian.setStok(stok2);
            pakaian.setKodePakaian(kode);

            //update pakaian
            PakaianDAO dao = new PakaianDAOImpl();
            boolean sukses = dao.ubahPakaian(pakaian);

            //cek sukses update
            if (sukses) {
                JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal diubah");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Silakan isi data pegawai");
        }

    }//GEN-LAST:event_btnUbahActionPerformed

