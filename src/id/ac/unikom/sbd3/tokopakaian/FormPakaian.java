    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int baris = tablePakaian.getSelectedRow();
        if (baris >= 0) {
            //mengambil data pakaian dari baris tabel
            Pakaian pakaianTerpilih = arrayPakaian.get(baris);

            //munculkan form
            FormUbahPakaian ftu = new FormUbahPakaian(null, true, pakaianTerpilih);
            ftu.setLocationRelativeTo(null);
            ftu.setVisible(true);
        } else{
            JOptionPane.showMessageDialog(this, "Pilih Pakaian yang akan diubah");
        }

    }//GEN-LAST:event_btnUbahActionPerformed

