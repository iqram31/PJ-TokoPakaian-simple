    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = tablePakaian.getSelectedRow();
        if (baris >= 0) {
            Pakaian PakaianTerpilih = arrayPakaian.get(baris);

            int aksiUser = JOptionPane.showConfirmDialog(this, "Apakah Anda "
                    + "yakin akan menghapus data "
                    + PakaianTerpilih.getNama(), "Hapus Pakaian?",
                    JOptionPane.YES_NO_OPTION);
            if (aksiUser == JOptionPane.YES_NO_OPTION) {
                String kode = PakaianTerpilih.getKodePakaian();
                PakaianDAO dao = new PakaianDAOImpl();
                boolean sukses;
                sukses = dao.hapusPakaian(kode);

                if (sukses) {
                    JOptionPane.showMessageDialog(this, "Pakaian berhasil dihapus");
                } else {
                    JOptionPane.showMessageDialog(this, "Pakaian gagal dihapus");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Pilih Pakaian yang akan dihapus");
            }
            getData(null);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        String nama = fieldCari.getText();
        if (nama != null && !nama.isEmpty()) {
            getData(nama);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Silakan isi nama pakaian pada kolom pencarian");
        }
    }//GEN-LAST:event_btnCariActionPerformed

