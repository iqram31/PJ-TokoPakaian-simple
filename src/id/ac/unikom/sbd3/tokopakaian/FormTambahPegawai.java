    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // mengambil data dari form
        String username = fieldUserName.getText();
        String password = String.valueOf(fieldPass.getPassword());
        String nip = fieldNip.getText();
        String nama = fieldNama.getText();
        String jenisKelamin;
        String akses;

        if (radioPria.isSelected()) {
            jenisKelamin = radioPria.getText();
        } else {
            jenisKelamin = radioWanita.getText();
        }
        if (radioAdmin.isSelected()) {
            akses = "admin";
        } else {
            akses = "operator";
        }
        String alamat = fieldAlamat.getText();
        
        for (int i = 0; i < arrayPegawai.size(); i++) {
            if (nip.equalsIgnoreCase(arrayPegawai.get(i).getNip())) {
                sama = true;
            }
        }

        //Validasi nip, nama, jenis kelamin, alamt, no hp
        if ((username != null && !username.isEmpty())
                && (password != null && !password.isEmpty())
                && (nip != null && !nip.isEmpty())
                && (nama != null && !nama.isEmpty())
                && (jenisKelamin != null && !jenisKelamin.isEmpty())
                && (alamat != null && !alamat.isEmpty()) && !sama) {

            Pegawai pegawai = new Pegawai();
            pegawai.setUserName(username);
            pegawai.setPassWord(password);
            pegawai.setHakAkses(akses);
            pegawai.setNip(nip);
            pegawai.setNama(nama);
            pegawai.setJenisKelamin(jenisKelamin);
            pegawai.setAlamat(alamat);

            //Memasukkan pegawai
            PegawaiDAO dao = new PegawaiDAOImpl();
            boolean sukses = dao.tambahPegawai(pegawai);

            //Mengecek berhasil atau tidak
            if (sukses) {
                JOptionPane.showMessageDialog(this, "Data Pegawai berhasil ditambahkan");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Data Pegawai gagal ditambahkan");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Silakan isi data Pegawai dan NIP tidak"
                    + " boleh sama!");
            sama = false;
        }
    }//GEN-LAST:event_btnTambahActionPerformed

