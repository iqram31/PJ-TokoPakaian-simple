    private void getData() {
        ResellerDAO dao = new ResellerDAOImpl();
        arrayReseller = dao.getReseller();

        ResellerTM tm = new ResellerTM();
        tm.setArrayReseller(arrayReseller);
        tableReseller.setModel(tm);
    }

    private void getData(String nama) {
        ResellerDAO dao = new ResellerDAOImpl();
        arrayReseller = dao.getReseller(nama);

        ResellerTM tm = new ResellerTM();
        tm.setArrayReseller(arrayReseller);
        tableReseller.setModel(tm);
    }

    private void refreshData() {
        getData();
    }

