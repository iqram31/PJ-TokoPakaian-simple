    private void btnCariPakaianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariPakaianActionPerformed
        FormCariPakaian fcp = new FormCariPakaian(null, true);
        fcp.setLocationRelativeTo(null);
        fcp.setVisible(true);

        pakaian = fcp.getPakaian();
        if (pakaian != null) {
            lblKode.setText(pakaian.getKodePakaian());
            lblNama.setText(pakaian.getNama());
            lblHarga.setText(String.valueOf(pakaian.getHarga()));
            stok = pakaian.getStok();
        }
    }//GEN-LAST:event_btnCariPakaianActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        String kodePakaian = lblKode.getText();
        String jumlah = fieldJumlah.getText();
        String harga = lblHarga.getText();
        Date date = dateTransaksi.getDate();

        if ((kodePakaian != null && !kodePakaian.isEmpty())
                && (jumlah != null && !jumlah.isEmpty())
                && (harga != null && !harga.isEmpty())
                && (date != null)) {
            hrg = Integer.valueOf(harga);
            jml = Integer.valueOf(jumlah);
            int hrgTotal = hrg * jml;

            if (stok != 0 && jml <= stok) {
                stok = stok - jml;
                // validasi tambah transaksi
                TransaksiKonsumen transKonsumen = new TransaksiKonsumen();
                transKonsumen.setTglTrans(date);
                transKonsumen.setJumlah(jml);
                transKonsumen.setHarga(hrg);
                transKonsumen.setHargaTotal(hrgTotal);
                transKonsumen.setKodePakaian(kodePakaian);
                transKonsumen.setNip(nip);

                //validasi pengurangan stok
                pakaian.setStok(stok);
                pakaian.setKodePakaian(kodePakaian);

                //insert transaksi
                TransaksiKonsumenDAO dao = new TransaksiKonsumenDAOImpl();
                PakaianDAO dao2 = new PakaianDAOImpl();
                boolean sukses = dao.trans(transKonsumen);
                boolean sukses2 = dao2.ubahStok(kodePakaian, stok);
                if (sukses && sukses2) {
                    JOptionPane.showMessageDialog(this, "Data Transaksi berhasil ditambahkan");
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Data Transaksi gagal ditambahkan");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Stok Tidak Cukup atau Kosong!");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Silakan isi data Transaksi");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void fieldJumlahKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fieldJumlahKeyPressed
        int key = evt.getKeyCode();
        if ((key >= evt.VK_0 && key <= evt.VK_9)
                || (key >= evt.VK_NUMPAD0 && key <= evt.VK_NUMPAD9)
                || (key == KeyEvent.VK_BACK_SPACE)) {
            fieldJumlah.setEditable(true);
            fieldJumlah.setBackground(Color.white);
        } else {
            fieldJumlah.setEditable(false);
            fieldJumlah.setBackground(Color.red);
        }
    }//GEN-LAST:event_fieldJumlahKeyPressed

