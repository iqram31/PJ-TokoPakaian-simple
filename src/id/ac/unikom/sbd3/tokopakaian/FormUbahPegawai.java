    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        // mengambil data dari form
        String username = fieldUserName.getText();
        String password = fieldPassword.getText();
        String nip = lblNIP.getText();
        String nama = fieldNama.getText();

        if (radioPria.isSelected()) {
            jenisKelamin = radioPria.getText();
        } else {
            jenisKelamin = radioWanita.getText();
        }

        if (radioAdmin.isSelected()) {
            hakAkses = radioAdmin.getText();
        } else {
            hakAkses = radioOperator.getText();
        }

        String alamat = areaAlamat.getText();

        if ((username != null && !username.isEmpty())
                && (password != null && !password.isEmpty())
                && (nip != null && !nip.isEmpty())
                && (nama != null && !nama.isEmpty())
                && (jenisKelamin != null && !jenisKelamin.isEmpty())
                && (alamat != null && !alamat.isEmpty())) {

            Pegawai pegawai = new Pegawai();
            pegawai.setUserName(username);
            pegawai.setPassWord(password);
            pegawai.setHakAkses(hakAkses);
            pegawai.setNip(nip);
            pegawai.setNama(nama);
            pegawai.setJenisKelamin(jenisKelamin);
            pegawai.setAlamat(alamat);

            // insert anggota
            PegawaiDAO dao = new PegawaiDAOImpl();
            boolean sukses = dao.ubahPegawai(pegawai);

            // cek sukses atau tidak
            if (sukses) {
                JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal diubah");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Silakan isi data pegawai");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

