    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String idReseller = lblIdReseller.getText();
        String kodePakaian = lblKodePakaian.getText();
        String jumlah = fieldJumlah.getText();
        String harga = lblHarga.getText();

        Date date = dateTransaksiReseller.getDate();

        if ((idReseller != null && !idReseller.isEmpty())
                && (kodePakaian != null && !kodePakaian.isEmpty())
                && (jumlah != null && !jumlah.isEmpty())
                && (harga != null && !harga.isEmpty())
                && (date != null)) {
            hrg = Integer.valueOf(harga);
            jml = Integer.valueOf(jumlah);
            int hrgTotal = hrg * jml;

            if (stok != 0 && jml <= stok) {
                stok = stok - jml;
                // validasi tambah transaksi
                TransaksiReseller transReseller = new TransaksiReseller();
                transReseller.setTglTrans(date);
                transReseller.setJumlah(jml);
                transReseller.setHarga(hrg);
                transReseller.setHargaTotal(hrgTotal);
                transReseller.setKodePakaian(kodePakaian);
                transReseller.setIdReseller(idReseller);
                transReseller.setNip(nip);

                //validasi pengurangan stok
                pakaian.setStok(stok);
                pakaian.setKodePakaian(kodePakaian);

                //insert transaksi
                TransaksiResellerDAO dao = new TransaksiResellerDAOImpl();
                PakaianDAO dao2 = new PakaianDAOImpl();
                boolean sukses = dao.trans(transReseller);
                boolean sukses2 = dao2.ubahStok(kodePakaian, stok);
                if (sukses && sukses2) {
                    JOptionPane.showMessageDialog(this, "Data Transaksi berhasil ditambahkan");
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Data Transaksi gagal ditambahkan");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Stok Tidak Cukup atau Kosong!");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Silakan isi data Transaksi");
        }


    }//GEN-LAST:event_btnSimpanActionPerformed

