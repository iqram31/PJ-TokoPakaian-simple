    private void btnPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihActionPerformed
        int baris = tablePakaian.getSelectedRow();
        if (baris >= 0) {
            Pakaian = arrayPakaian.get(baris);
            dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Silakan pilih Anggota");
        }
    }//GEN-LAST:event_btnPilihActionPerformed

