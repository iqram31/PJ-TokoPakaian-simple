    private void getData() {
        PakaianDAO dao = new PakaianDAOImpl();
        arrayPakaian = dao.getPakaian();

        PakaianTM tmp = new PakaianTM();
        tmp.setArrayPakaian(arrayPakaian);
        tablePakaian.setModel(tmp);
    }

    private void getData(String nama) {
        PakaianDAO dao = new PakaianDAOImpl();
        arrayPakaian = dao.getPakaian(nama);

        PakaianTM tmp = new PakaianTM();
        tmp.setArrayPakaian(arrayPakaian);
        tablePakaian.setModel(tmp);

    }

    private void refreshData() {
        getData();
    }

